

# python program to illustrate different set operations 

# defining two sets 

E={1,2,3,4,5}

N={0,1,3,4,5}

# set union

print(f"Union of set {E} and set {N}  is ", E|N)

#set intersection 

print(f"Intersection of set {E} and set {N}  is ", E&N)

# set difference 

print(f"Difference  of set {E} and set {N}  is ", E-N)

# set symmetric difference

print(f"Symmetric Difference of set {E} and set {N}  is ", E^N)




